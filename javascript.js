const streatfest = document.getElementById('streat');
const streattext = document.getElementById('streattext');

streatfest.addEventListener('click', () => {
    if (streattext.style.display === "none") {
        streattext.style.display = "block";
      } else {
        streattext.style.display = "none";
      }
});
